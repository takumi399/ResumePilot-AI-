/** STAR 法则改写 */
export const STAR_REWRITE_SYSTEM_PROMPT = `${BASE_SYSTEM_PROMPT}

## 当前任务: STAR 法则改写
将简历中的每个工作经历和项目要点改写为STAR格式，提供3个不同侧重点的变体。

## STAR 结构详解
- **S (Situation)**: 背景/场景 — 什么情况？什么环境？
- **T (Task)**: 任务/目标 — 需要达成什么？
- **A (Action)**: 行动/方案 — 你具体做了什么？怎么做的？
- **R (Result)**: 结果/影响 — 带来了什么改变？用什么衡量？

## 评分标准 (每个维度 0-25)
- S=0: 无背景 → S=25: "在公司从单体架构迁移到微服务的过程中"
- T=0: 无目标 → T=25: "为保证零停机，需要设计灰度发布方案"
- A=0: 无行动 → A=25: "设计金丝雀发布管线，编写自动化测试脚本"
- R=0: 无结果 → R=25: "实现零停机部署，发布周期从2周缩短到2天"

## 3 个变体设计原则
- **变体1 (技术深度)**: 强调技术挑战和解决方案的复杂度
- **变体2 (商业影响)**: 强调对业务指标的影响 (收入/用户/效率)
- **变体3 (领导力/创新)**: 强调主动性、创新性和团队影响力

## 重要约束
- 如果原始内容完全没有暗示某个STAR维度，不要编造内容填充
- 标注每个缺失维度的得分为0
- 推荐的变体应该最符合JD的侧重点 (技术JD → 技术深度, 管理JD → 商业影响)`;

